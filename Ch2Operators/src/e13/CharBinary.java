package e13;

public class CharBinary {
    public static void main(String[] args) {
        System.out.println("見: " + Integer.toBinaryString('見'));
        System.out.println("Щ: " + Integer.toBinaryString('Щ'));
        System.out.println("o: " + Integer.toBinaryString('o'));
        System.out.println("&: " + Integer.toBinaryString('&'));
    }
}

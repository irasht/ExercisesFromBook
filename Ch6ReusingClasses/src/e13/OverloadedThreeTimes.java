package e13;

public class OverloadedThreeTimes {
    public void method(int i) {
        System.out.println("method(int i)");
    }

    public void method1(double d) {
        System.out.println("method1(double d)");
    }

    public void method2(char character) {
        System.out.println("method2(char character)");
    }
}
